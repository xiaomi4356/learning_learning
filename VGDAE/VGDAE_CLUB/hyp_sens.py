def visualize_Channels(list1, list2, list3):
    plt.figure(dpi=600)
    #修改图中字体
    plt.rc('font', family='Times New Roman')
    #修改字号
    plt.xticks(fontsize=9)
    plt.yticks(fontsize=9)
    plt.tick_params(labelsize=9)
    #设置坐标轴名称
    plt.xlabel('Number of channels')
    plt.ylabel('AUC(%)')
    plt.xticks(np.arange(0, 32, 2))
    plt.plot(list1, 'D', linestyle='--', linewidth=1, label='Cora')
    plt.plot(list2, 'o', linestyle='-',  linewidth=1, label='Citeseer')
    plt.plot(list3, 'x', linestyle='-',  linewidth=1, label='Pubmed')
    plt.grid(visible=True, linestyle = ':')
    plt.legend(loc='lower right')
    plt.savefig("channels.eps", dpi=600, format='eps')
